import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error, r2_score
import pickle

# Load the data from the CSV file
data = pd.read_csv('Book1 (1).csv')  # Replace with your CSV file path

# Prepare the data
X = data[['No.of Guests']]
y = data['No.of Balloons needed']

# Train the linear regression model
model = LinearRegression()
model.fit(X, y)

# Predict the values
y_pred = model.predict(X)

# Calculate R-squared
r_squared = r2_score(y, y_pred)

# Calculate Mean Squared Error (MSE)
mse = mean_squared_error(y, y_pred)

# Print model information
print('Model Coefficients (guests):', model.coef_)
print('Model Intercept:', model.intercept_)
print('R-squared:', r_squared)
print('Mean Squared Error (MSE):', mse)

# Visualize the best-fit line
plt.scatter(X, y, color='blue', label='Actual Data')
plt.plot(X, model.predict(X), color='red', label='Best Fit Line')
plt.xlabel('Number of Guests')
plt.ylabel('Number of Helium Balloons')
plt.title('Best Fit Line for Predicting Balloons based on Guests')
plt.legend()
plt.show()

pickle.dump(model,open("model.pkl","wb"))